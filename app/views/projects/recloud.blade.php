<h1>reCloud</h1>
{{ HTML::image('assets/img/recloud-org.png', 'reCloud', array('style' =>'width:100%')) }}
<p>reCloud aims to be a new way to Self-Host your own Cloud Storage. It will allow you to run the application on shared hosting, and link it to a remote cloud storage solution, such as Amazon Web Services, or DreamObjects etc.</p>

<p>This is still in extremely early pre-alpha, so is not ready yet.</p>

